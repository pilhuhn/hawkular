package org.hawkular.opsqueue;

/**
 * One resolved item
 *
 * @author Heiko W. Rupp
 */
public class ResolvedItem {


    OpsItem item;
    String result;

    public ResolvedItem(OpsItem item) {
        this.item = item;
    }

    public OpsItem getItem() {
        return item;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getId() {
        return item.getId();
    }
}
